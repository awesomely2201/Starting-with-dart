void main() {
  int a = 10;
  print(a.isEven);//returns true if even
  print(a.hashCode);//returns hashcode value of that integer
  print(a.isFinite);//returns true if finite
  print(a.isInfinite);//returns true if infinite
  print(a.isOdd);//returns true if odd
  print(a.isNaN);//returns true if not a number
  print(a.isNegative);//returns true if negative
}
